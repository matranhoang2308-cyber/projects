const STORAGE_KEY = "crm_customer_bookings";

export function getStoredBookings(): CustomerBooking[] {
  if (typeof window === "undefined") return initialBookings;
  const stored = localStorage.getItem(STORAGE_KEY);
  if (!stored) {
    localStorage.setItem(STORAGE_KEY, JSON.stringify(initialBookings));
    return initialBookings;
  }
  try {
    const list: CustomerBooking[] = JSON.parse(stored);
    // Sanitize any invalid negative conBoSung records from old mock data
    const sanitized = list.map((b) => {
      const phaiThu = b.phaiThu || 100000000;
      const daThu = b.daThu > phaiThu ? 50000000 : b.daThu;
      const conBoSung = phaiThu - daThu;
      return {
        ...b,
        phaiThu,
        daThu,
        conBoSung,
      };
    });
    return sanitized;
  } catch {
    return initialBookings;
  }
}

export function saveStoredBookings(bookings: CustomerBooking[]): void {
  if (typeof window !== "undefined") {
    localStorage.setItem(STORAGE_KEY, JSON.stringify(bookings));
  }
}
